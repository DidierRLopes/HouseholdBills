    private void daySBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_daySBMouseClicked
        int yearIndice = yearSB.getSelectedIndex();
        int monthIndice = monthSB.getSelectedIndex();
        int dayIndice = daySB.getSelectedIndex();
        
        if(yearIndice != -1 && monthIndice != -1 && dayIndice != -1){
            nameSB.setText(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).nameBill);
            commentSB.setText(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).commentBill);
            totalValueSB.setText(Double.toString(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).totalValue));

            if(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).nameBill.contains("empresta"))
                lblTotalValueSB.setText("Empréstimo de:");
            else
                if(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).nameBill.contains("paga"))
                    lblTotalValueSB.setText("Pagmento de:");
                else
                    lblTotalValueSB.setText("Compra de:");

            if(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).nameBill.contains("Patrick ")){
                personASB.setEnabled(false);
                personBtoPersonASB.setEnabled(true);
                personBSB.setEnabled(true);
                personAtoPersonBSB.setEnabled(false);

                articlesPersonA.setEnabled(false);
                euroApers.setEnabled(false);
                articlesPersonB.setEnabled(false);
                euroBpers.setEnabled(false);
                billPersonBSB.setEnabled(false);

                billPersonASB.setText("0.00");
                billPersonASB.setEnabled(false);
                billPersonBSB.setText("0.00");
                billPersonASB.setEnabled(false);
            }
            else
                if(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).nameBill.contains("Didier ")){
                    personASB.setEnabled(true);
                    personBtoPersonASB.setEnabled(false);
                    personBSB.setEnabled(false);
                    personAtoPersonBSB.setEnabled(true);

                    articlesPersonA.setEnabled(false);
                    euroApers.setEnabled(false);
                    articlesPersonB.setEnabled(false);
                    euroBpers.setEnabled(false);
                    billPersonBSB.setEnabled(false);

                    billPersonASB.setText("0.00");
                    billPersonASB.setEnabled(false);
                    billPersonBSB.setText("0.00");
                    billPersonASB.setEnabled(false);
                }
                else{
                    if(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).valuePersonB == 0.00){
                        personASB.setEnabled(true);
                        personBSB.setEnabled(false);
                    }
                    else
                        if(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).valuePersonA == 0.00){
                        personASB.setEnabled(false);
                        personBSB.setEnabled(true);
                        }
                        else{
                            personASB.setEnabled(true);
                            personBSB.setEnabled(true);
                        }

                        personBtoPersonASB.setEnabled(false);
                        personAtoPersonBSB.setEnabled(false);

                        articlesPersonA.setEnabled(true);
                        euroApers.setEnabled(true);
                        articlesPersonB.setEnabled(true);
                        euroBpers.setEnabled(true);

                        billPersonASB.setEnabled(true);
                        billPersonASB.setText(Double.toString(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).valuePersonA));
                        billPersonBSB.setEnabled(true);
                        billPersonBSB.setText(Double.toString(Bills.ALL.get(yearIndice).months.get(monthIndice).bills.get(dayIndice).valuePersonB));
                }
            }
        else{
            ImageIcon frenchieNOTOK = new ImageIcon(TestOption.class.getResource("/frenchieNOTOK.png"));
            JOptionPane.showMessageDialog(null, "Selecionar ano, mês e dia válidos!", "Aviso de Insucesso :(", 0, frenchieNOTOK);
        }
    }//GEN-LAST:event_daySBMouseClicked

