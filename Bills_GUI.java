        JPanel panel = new JPanel();
        ImageIcon frenchie = new ImageIcon(TestOption.class.getResource("/frenchie.png"));
	panel.add(label);
        panel.add(password);
        Object[] options = { "OK", "Cancelar"};
        int option = 0;
        while (option == 0){
            password.setText("");
            
            option = JOptionPane.showOptionDialog(null, panel, "Acesso restrito a Didier", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, frenchie, options, options[0]);
            
            if(option == 0){
                String pass = password.getText();
                
                if (!pass.equals("patachoca")){
                    ImageIcon frenchieNOTOK = new ImageIcon(TestOption.class.getResource("/frenchieNOTOK.png"));
                    JOptionPane.showMessageDialog(null, "A password inserida está incorreta!", "Aviso de Insucesso :(", 0, frenchieNOTOK);
                }
                else{
                    paymentPersonANB.setVisible(false);
                    symbolEuroA.setVisible(false);
                    paymentPersonBNB.setVisible(false);
                    symbolEuroB.setVisible(false);

                    newBillFrame.pack();  
                    newBillFrame.setVisible(rootPaneCheckingEnabled);
                    dateNB.setText(new SimpleDateFormat("dd/MM/yyyy").format(new Date(System.currentTimeMillis())));
                    option = 1;
                }
            }
        }   
